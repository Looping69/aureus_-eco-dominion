const colorCache: Record<string, THREE.Material> = {};

function getMat(hex: string) {
    if (!colorCache[hex]) {
        colorCache[hex] = new THREE.MeshStandardMaterial({ 
            color: new THREE.Color(hex), 
            roughness: 0.8,
            metalness: 0.1
        });
    }
    return colorCache[hex];
}

export function createEagle(): THREE.Group {
    const group = new THREE.Group();
    const body = new THREE.Group();
    const leftWing = new THREE.Group();
    const rightWing = new THREE.Group();

    // Scale down the giant bird to map scale
    // 30 units wide -> 1.5 units wide
    group.scale.setScalar(0.05);

    // Pivot logic: Wings flap relative to shoulder
    // Left Wing (x < -2) shoulder at approx x=-2
    // Right Wing (x > 2) shoulder at approx x=2
    
    // Position wing groups at shoulders
    leftWing.position.set(-2, 13, 0); 
    rightWing.position.set(2, 13, 0);
    
    // Position body at same pivot height to align voxels correctly
    body.position.set(0, 13, 0);
    
    leftWing.name = 'wingL';
    rightWing.name = 'wingR';

    EAGLE_DATA.forEach(v => {
        // Offset Y to center vertically around 0 relative to pivot
        const y = v.y - 13;
        
        const mat = getMat(v.c);
        const mesh = voxel(1, 1, 1, mat, 0, 0, 0); // Position set below

        if (v.x < -2.5) {
            // Add to Left Wing
            // Relative position to shoulder (-2, 13, 0)
            mesh.position.set(v.x - (-2), y, v.z);
            leftWing.add(mesh);
        } else if (v.x > 2.5) {
            // Add to Right Wing
            // Relative position to shoulder (2, 13, 0)
            mesh.position.set(v.x - 2, y, v.z);
            rightWing.add(mesh);
        } else {
            // Body
            mesh.position.set(v.x, y, v.z);
            body.add(mesh);
        }
    });

    group.add(body);
    group.add(leftWing);
    group.add(rightWing);
    
    // Tag for engine
    group.userData = { isAnimatedEagle: true };

    return group;
}
